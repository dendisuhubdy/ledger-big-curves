def pretty_print(big_int):
    big_bytes = big_int.to_bytes(96, 'big')
    chunks = []
    len_bytes = len(big_bytes)//8
    for i in range(len_bytes):
        int_int = int.from_bytes(big_bytes[i*8:(i+1)*8], 'big', signed=False)
        chunks.append(f'{int_int:020}')
    return chunks

